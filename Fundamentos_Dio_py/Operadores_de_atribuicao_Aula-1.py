#São Utilizados para definir o valor inicial ou sobrescrever o valor de uma variável.

#atribuição simples 

saldo = 500

#atribuição com adição 

saldo = 500
saldo += 200 

print(saldo)

#atribuição com subtração 

saldo = 500
saldo -= 100

print(saldo)

#atribuição com multiplicação 

saldo = 500
saldo *= 2

print(saldo)

#atribuição com divisão e divisão inteira 

saldo = 500 
saldo /= 5

print(saldo)

#inteira

saldo = 500 
saldo //= 5

print(saldo)

#Atribuição com Exponenciação 

saldo = 80
saldo **= 2

print(saldo)
